/**
 * Populate scripts/data/cannes-subcategories-by-year.json from
 * data/processed/cannes_taxonomy_by_year.json (canonical subcategory labels = level3 lines).
 *
 * Usage: node scripts/build-cannes-subcategories-by-year-data.mjs
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

function main() {
  const taxPath = path.join(ROOT, "data", "processed", "cannes_taxonomy_by_year.json");
  if (!fs.existsSync(taxPath)) {
    console.error(`Missing ${taxPath}`);
    process.exit(1);
  }
  const tax = JSON.parse(fs.readFileSync(taxPath, "utf8"));
  const bySlug = {};

  for (const [ys, block] of Object.entries(tax.byYear || {})) {
    const year = parseInt(ys, 10);
    if (!Number.isFinite(year)) continue;
    for (const [slug, rec] of Object.entries(block.bySlug || {})) {
      const pairs = rec?.facet_pairs;
      if (!Array.isArray(pairs) || !pairs.length) continue;
      const labels = [
        ...new Set(
          pairs.map((p) => String(p.level3 ?? "").trim()).filter(Boolean),
        ),
      ].sort((a, b) => a.localeCompare(b, "en"));
      if (!labels.length) continue;
      if (!bySlug[slug]) bySlug[slug] = {};
      bySlug[slug][String(year)] = labels;
    }
  }

  const out = {
    _note:
      "Generated by scripts/build-cannes-subcategories-by-year-data.mjs from hub taxonomy. Full strings with codes (e.g. A01 - …) per slug-year for resolveCanonicalSubcategory.",
    bySlug,
  };

  const outPath = path.join(ROOT, "scripts", "data", "cannes-subcategories-by-year.json");
  fs.writeFileSync(outPath, JSON.stringify(out, null, 2) + "\n", "utf8");
  console.log(`Wrote ${outPath} (${Object.keys(bySlug).length} slugs)`);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main();
}
